class Loop{
  public static void main(String args[]){

    int i = 1;
    while(i<=5){
      System.out.println("Hello");
      i++;
    }
    System.out.println(i);

    for(i=1;i<=5;i++)
    {
      System.out.println("Bye");
    }
    System.out.println(i);
  i=1;
  do{
    System.out.println("OK");
    i++;
  }while(i<=5);
  System.out.println(i);
}
}

